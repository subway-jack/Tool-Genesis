class ResearchReport(BaseResponseFormat):
    """Structured output format for the Research agent, containing three standardized path modules:
    1. research_report_path: path to the research agent's report file (string)
    2. python_code_path: path to the Python code file generated by the research agent (string)
    3. env_state_desc_path: path to the JSON data file describing the environment state generated by the research agent (string)
    """
    research_report_content: str = Field(..., description="content of the research agent's report file (string)")

    
    @field_validator("*")
    @classmethod
    def _non_empty_path(cls, v: str) -> str:
        if not isinstance(v, str) or not v.strip():
            raise ValueError("path must be a non-empty string")
        return v.strip()
    
    def info(self) -> str:
        """
        Return a string representation of the ResearchReport object.
        
        Returns:
            str: A string containing the paths to the research report, Python code, and environment state description.
        """
        return f"Research Report Content: {self.research_report_content}\n"

