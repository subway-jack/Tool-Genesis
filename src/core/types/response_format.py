class InitAndSeedTasks(BaseResponseFormat):
    init_state: str = Field(..., description="Initialization state object for the server (stringified JSON)")
    seed_tasks: List[SeedTaskItem] = Field(..., description="List of seed tasks with tool info and analysis")
    domain_mode: str = Field(..., description="Domain mode classification: 'fixed_domain' or 'general_tool'")

    @field_validator("init_state")
    @classmethod
    def _coerce_init_state(cls, v: Any) -> str:
        if isinstance(v, str):
            return v.strip()
        try:
            return json.dumps(v or {}, ensure_ascii=False)
        except Exception:
            return json.dumps({}, ensure_ascii=False)

    @field_validator("domain_mode")
    @classmethod
    def _validate_domain_mode(cls, v: Any) -> str:
        if isinstance(v, str):
            s = v.strip().lower()
            if s in ("fixed_domain", "general_tool"):
                return s
        return "general_tool"

class ScoreAnalysis(BaseResponseFormat):
    score: int = Field(..., description="Score from 1 to 5")
    analysis: str = Field(..., description="Short explanation")

    @field_validator("score")
    @classmethod
    def _validate_score(cls, v: int) -> int:
        if not isinstance(v, int):
            raise ValueError("score must be an integer")
        if v < 1 or v > 5:
            raise ValueError("score must be between 1 and 5")
        return v

    @field_validator("analysis")
    @classmethod
    def _non_empty_analysis(cls, v: str) -> str:
        if not isinstance(v, str) or not v.strip():
            raise ValueError("analysis must be a non-empty string")
        return v.strip()

