class ListTask(BaseResponseFormat):
    tasks: List[Task] = Field(..., description="A list of tasks.")
    
    def info(self) -> str:
        """
        Return a string representation of the ListTask object.
        
        Returns:
            str: A string containing the descriptions of all tasks.
        """
        return "\n".join([f"Task: {task.task_description}" for task in self.tasks])
    def to_json(self) -> str:
        """
        Convert the ListTask object to a JSON string.
        
        Returns:
            str: A JSON string representation of the ListTask object.
        """
        return json.dumps(self.dict())

