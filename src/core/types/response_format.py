class BaseResponseFormat(BaseModel):
    """Base class for response formats.
    
    This class provides a common interface for all response formats,
    allowing for easy concatenation of attributes into a single string.
    """
    
    class Config:
        extra = "forbid"  # 这会在JSON Schema中设置 additionalProperties: false
    
    def info(self) -> str:
        """
        Concatenate all attributes into a single string.
        Subclasses can override this method to customize concatenation logic.
        
        Returns:
            str: The concatenated string
        """
        attributes = []
        for field_name, field_value in self.model_dump().items():
            if isinstance(field_value, (str, int, float, bool)):
                attributes.append(f"{field_name}: {field_value}")
            elif isinstance(field_value, list):
                attributes.append(f"{field_name}: {', '.join(map(str, field_value))}")
            elif isinstance(field_value, dict):
                dict_str = ', '.join([f"{k}: {v}" for k, v in field_value.items()])
                attributes.append(f"{field_name}: {{{dict_str}}}")
            elif field_value is not None:
                attributes.append(f"{field_name}: {str(field_value)}")
        
        return "\n".join(attributes)

    @classmethod
    def json_info(cls) -> str:
        """
        Return the JSON Schema of this response format as a plain text string.
        """
        schema = cls.model_json_schema()
        return json.dumps(schema, ensure_ascii=False, indent=2)

